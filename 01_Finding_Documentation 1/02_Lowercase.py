# Determine whether Python has a method to lowercase a string, for example 
# converting 'Aloha, World!' into 'aloha, world!.


# Standard Library -> Built-in Types -> Text Sequence Types - str ->
#                                                               String Methods

# str.lower()       # return a copy in lowercase

string = 'Aloha, World!'
print(string.lower())

print('Aloha, World!'.lower())      # aloha, world!