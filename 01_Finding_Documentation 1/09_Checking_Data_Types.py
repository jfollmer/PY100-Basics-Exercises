# In the Reading Documentation 1 exercises:

# Referring to the official Python documentation, how would you identify the 
# data type of the following values?

23.5
'Call me Ishmael.'
False
0
None

# I couldn't find this by searching the documentation. Solution says to look
# under Standard Library -> Built-in Functions -> type().

print(type(23.5))
print(type('Call me Ishmael.'))
print(type(False))
print(type(0))
print(type(None))