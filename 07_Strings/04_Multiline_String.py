# Multiline String exercise in Strings exercises:

"""How can you assign the following rhyme to a single variable while 
preserving the line break?

A pirate I was meant to be!
Trim the sails and roam the sea!
"""


string = """A pirate I was meant to be! 
Trim the sails and roam the sea!"""

print(string)


"""The given solution includes a version with a newline escape sequence,
although I didn't go with that since it's harder to read.
"""

rhyme = "A pirate I was meant to be!\nTrim the sails and roam the sea!"

print(rhyme)
# A pirate I was meant to be!
# Trim the sails and roam the sea!